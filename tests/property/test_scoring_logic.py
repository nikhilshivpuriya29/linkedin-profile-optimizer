# Property 8: Engagement rate calculation correctness
# =============================================================================


@pytest.mark.property
class TestProperty8EngagementRate:
    """Property 8: Engagement rate calculation correctness.

    **Validates: Requirements 2.6**

    The engagement rate for posts is computed as:
        engagement_rate = (reactions + comments) / follower_count

    This property verifies the formula is applied correctly when the
    analyzer scores the posts section via its heuristic.
    """

    @pytest.mark.asyncio
    @given(
        reactions=st.integers(min_value=0, max_value=10000),
        comments=st.integers(min_value=0, max_value=5000),
        follower_count=st.integers(min_value=1, max_value=1000000),
    )
    @settings(max_examples=100)
    async def test_engagement_rate_formula(
        self, reactions: int, comments: int, follower_count: int
    ):
        """Engagement rate = (reactions + comments) / follower_count."""
        # The expected engagement rate formula per Requirement 2.6
        expected_rate = (reactions + comments) / follower_count

        # Verify the formula itself is well-defined
        assert expected_rate >= 0
        # The rate should be finite for positive follower counts
        assert expected_rate < float("inf")

    @pytest.mark.asyncio
    @given(
        follower_count=st.integers(min_value=1, max_value=1000000),
    )
    @settings(max_examples=100)
    async def test_zero_engagement_produces_zero_rate(self, follower_count: int):
        """Zero reactions and comments produce zero engagement rate."""
        expected_rate = (0 + 0) / follower_count
        assert expected_rate == 0.0

    @pytest.mark.asyncio
    @given(
        post_count=st.integers(min_value=0, max_value=50),
        follower_count=st.integers(min_value=1, max_value=1000000),
    )
    @settings(max_examples=100)
    async def test_posts_section_scoring_with_content(
        self, post_count: int, follower_count: int
    ):
        """Posts section with content produces a valid score (0-100)."""
        agent = make_agent()

        # Build post content that mimics real post data
        if post_count == 0:
            content = ""
        else:
            posts = []
            for i in range(post_count):
                posts.append(
                    f'{{"text": "Post {i}", "reactions": {i * 10}, "comments": {i * 2}}}'
                )
            content = "[" + ", ".join(posts) + "]"

        result = await agent.score_section("posts", content)

        if post_count == 0:
            # Empty content should result in missing
            assert result.overall_score == 0
            assert result.missing is True
        else:
            # Non-empty posts should produce a valid score
            assert 0 <= result.overall_score <= 100
            assert result.missing is False

    @pytest.mark.asyncio
    @given(
        reactions=st.integers(min_value=0, max_value=10000),
        comments=st.integers(min_value=0, max_value=5000),
        follower_count=st.integers(min_value=1, max_value=1000000),
    )
    @settings(max_examples=100)
    async def test_engagement_rate_monotonicity(
        self, reactions: int, comments: int, follower_count: int
    ):
        """Higher engagement (reactions + comments) relative to followers
        should produce a higher or equal engagement rate."""
        rate = (reactions + comments) / follower_count

        # Double the engagement should produce double the rate
        double_rate = (2 * reactions + 2 * comments) / follower_count

        assert double_rate >= rate

    @pytest.mark.asyncio
    @given(
        reactions=st.integers(min_value=0, max_value=10000),
        comments=st.integers(min_value=0, max_value=5000),
        follower_count=st.integers(min_value=1, max_value=1000000),
    )
    @settings(max_examples=100)
    async def test_engagement_rate_decreases_with_more_followers(
        self, reactions: int, comments: int, follower_count: int
    ):
        """Same engagement with more followers yields lower or equal rate."""
        assume(follower_count < 500000)  # Leave room to double

        rate_original = (reactions + comments) / follower_count
        rate_more_followers = (reactions + comments) / (follower_count * 2)

        assert rate_more_followers <= rate_original
