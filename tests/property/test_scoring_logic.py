# Property 6: Section scoring produces valid weighted averages
# =============================================================================


@pytest.mark.property
class TestProperty6WeightedAverages:
    """Property 6: Section scoring produces valid weighted averages.

    **Validates: Requirements 2.1, 2.9**

    For any list of FactorScore objects with scores 0-100, the
    _calculate_weighted_average method should:
    - Return a value between 0 and 100
    - Equal round(sum(scores) / len(scores)) for all-available factors
    - Exclude factors with score < 0 from the calculation
    """

    @given(factors=st.lists(factor_score_strategy(), min_size=1, max_size=10))
    @settings(max_examples=200)
    def test_result_is_between_0_and_100(self, factors: list[FactorScore]):
        """Weighted average is always in the valid 0-100 range."""
        agent = make_agent()
        result = agent._calculate_weighted_average(factors)
        assert 0 <= result <= 100

    @given(factors=st.lists(factor_score_strategy(), min_size=1, max_size=10))
    @settings(max_examples=200)
    def test_result_equals_rounded_mean_for_all_available(
        self, factors: list[FactorScore]
    ):
        """For all-available factors (score >= 0), result equals rounded mean."""
        agent = make_agent()
        result = agent._calculate_weighted_average(factors)

        # All factors generated by factor_score_strategy have score 0-100 (all available)
        expected = round(sum(f.score for f in factors) / len(factors))
        expected = max(0, min(100, expected))

        assert result == expected

    @given(
        factors=st.lists(
            factor_score_with_exclusions_strategy(), min_size=1, max_size=10
        )
    )
    @settings(max_examples=200)
    def test_excluded_factors_not_counted(self, factors: list[FactorScore]):
        """Factors with score < 0 are excluded from the weighted average."""
        agent = make_agent()
        result = agent._calculate_weighted_average(factors)

        # Compute expected excluding factors with score < 0
        available = [f for f in factors if f.score >= 0]

        if not available:
            assert result == 0
        else:
            expected = round(sum(f.score for f in available) / len(available))
            expected = max(0, min(100, expected))
            assert result == expected

    def test_empty_factors_returns_zero(self):
        """An empty list of factors produces a score of 0."""
        agent = make_agent()
        result = agent._calculate_weighted_average([])
        assert result == 0

    @given(score=st.integers(min_value=0, max_value=100))
    @settings(max_examples=100)
    def test_single_factor_equals_its_score(self, score: int):
        """A single available factor produces its own score as the average."""
        agent = make_agent()
        factor = FactorScore(factor_name="test", score=score, explanation="test")
        result = agent._calculate_weighted_average([factor])
        assert result == score


