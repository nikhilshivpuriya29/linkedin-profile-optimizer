"""Property-based tests for scoring logic (Properties 6, 7, 8).

**Validates: Requirements 2.1, 2.6, 2.8, 2.9**

These tests use Hypothesis to verify that the AnalyzerAgent's scoring
logic produces mathematically correct results, handles empty sections
appropriately, and computes engagement rates correctly.
"""

import pytest
from hypothesis import given, settings, assume
from hypothesis import strategies as st

from linkedin_optimizer.models import FactorScore
from linkedin_optimizer.agents.analyzer_agent import AnalyzerAgent


# --- Shared fixtures ---


def make_agent() -> AnalyzerAgent:
    """Create an AnalyzerAgent without an HF client (uses heuristic scoring)."""
    return AnalyzerAgent(
        model_id="test-model",
        fallback_model_id="test-fallback",
        hf_client=None,
    )


# --- Custom Strategies ---


@st.composite
def factor_score_strategy(draw):
    """Generate a FactorScore with score in 0-100 range."""
    return FactorScore(
        factor_name=draw(
            st.text(
                alphabet=st.characters(blacklist_categories=("Cs",)),
                min_size=1,
                max_size=50,
            )
        ),
        score=draw(st.integers(min_value=0, max_value=100)),
        explanation=draw(
            st.text(
                alphabet=st.characters(blacklist_categories=("Cs",)),
                min_size=0,
                max_size=100,
            )
        ),
    )


@st.composite
def factor_score_with_exclusions_strategy(draw):
    """Generate a FactorScore that may have score < 0 (excluded)."""
    return FactorScore(
        factor_name=draw(
            st.text(
                alphabet=st.characters(blacklist_categories=("Cs",)),
                min_size=1,
                max_size=50,
            )
        ),
        score=draw(st.integers(min_value=-1, max_value=100)),
        explanation=draw(
            st.text(
                alphabet=st.characters(blacklist_categories=("Cs",)),
                min_size=0,
                max_size=100,
            )
        ),
    )


