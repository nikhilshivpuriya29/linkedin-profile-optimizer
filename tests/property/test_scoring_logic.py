# =============================================================================


@pytest.mark.property
class TestProperty7EmptySections:
    """Property 7: Empty sections receive zero score with missing flag.

    **Validates: Requirements 2.8**

    When a section has empty or whitespace-only content, the score_section
    method should return score=0 and missing=True.
    """

    @pytest.mark.asyncio
    @given(
        section_name=st.sampled_from(
            ["headline", "about", "experience", "skills", "posts", "banner_photo"]
        )
    )
    @settings(max_examples=50)
    async def test_empty_string_produces_zero_score_and_missing(
        self, section_name: str
    ):
        """Empty string content yields score=0, missing=True."""
        agent = make_agent()
        result = await agent.score_section(section_name, "")
        assert result.overall_score == 0
        assert result.missing is True

    @pytest.mark.asyncio
    @given(
        section_name=st.sampled_from(
            ["headline", "about", "experience", "skills", "posts", "banner_photo"]
        ),
        whitespace=st.from_regex(r"\s+", fullmatch=True),
    )
    @settings(max_examples=50)
    async def test_whitespace_only_produces_zero_score_and_missing(
        self, section_name: str, whitespace: str
    ):
        """Whitespace-only content yields score=0, missing=True."""
        agent = make_agent()
        result = await agent.score_section(section_name, whitespace)
        assert result.overall_score == 0
        assert result.missing is True

    @pytest.mark.asyncio
    async def test_headline_empty_produces_zero_and_missing(self):
        """Specific check: empty headline yields score=0, missing=True."""
        agent = make_agent()
        result = await agent.score_section("headline", "")
        assert result.overall_score == 0
        assert result.missing is True
        assert result.section_name == "headline"

    @pytest.mark.asyncio
    async def test_about_empty_produces_zero_and_missing(self):
        """Specific check: empty about yields score=0, missing=True."""
        agent = make_agent()
        result = await agent.score_section("about", "")
        assert result.overall_score == 0
        assert result.missing is True
        assert result.section_name == "about"

    @pytest.mark.asyncio
    @given(
        section_name=st.sampled_from(
            ["headline", "about", "experience", "skills", "posts", "banner_photo"]
        )
    )
    @settings(max_examples=50)
    async def test_empty_section_has_all_factor_scores_at_zero(
        self, section_name: str
    ):
        """All factor scores within a missing section are 0."""
        agent = make_agent()
        result = await agent.score_section(section_name, "")
        for factor in result.factor_scores:
            assert factor.score == 0


